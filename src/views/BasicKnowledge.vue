    </p>

    <h4>24秒ルール</h4>

    <p>
      オフェンスの際はボールを取った時から、24秒以内にシュートを放たなければいけません。24秒経過する前にシュートを放ち、空中で24秒が経過後、そのシュートが決まった場合は、得点としてカウントされます。
    </p>

    <h4>8秒ルール</h4>

    <p>
      相手チームからボールを奪った瞬間から8秒以内にバックコート（自チーム）からフロントコート（相手チーム）へボールを進めなければなりません。
    </p>

    <h4>5秒ルール</h4>

    <p>
      スローイン、フリースロー、コート内でボールを受けた時は、受け取ってから5秒以内に、パス・ドリブル・シュートのいずれかのプレーをしなければなりません。
    </p>

    <h4>3秒ルール</h4>

    <p>
      相手チームのバスケット下の制限区域内に、連続して3秒以上とどまることはできません。
    </p>

    <h4>ダブルドリブル</h4>


      バスケットボールでペナルティをかせられる行為には、ファウルとバイオレーション（違反）の2種類があります。
    </p>

    <h3>ファウル</h3>

    <p>
      プレーヤー同士の接触によるものや、スポーツマンにふさわしくない行為・言動に対してはファウルが宣告され、相手チームにフリースローやスローインが与えられます。
    </p>

    <h3>パーソナル・ファウル</h3>

    <p>
      ブロッキングやチャージングなど、プレーヤー同士の接触に対してとられます。
    </p>

    <p>
      シュート動作中はフリースローが与えられ、それ以外のファウルの場合、相手チームがスローインをします。
    </p>

    <h3>5ファウル</h3>

    <p>
      同じプレーヤーが、1試合をとおして5つ目のファウルを犯した場合は退場し、ベンチのプレーヤーと交代しなければなりません。
    </p>

    <h3>チームファウル</h3>

    <p>
      各クオーターでチームの累計ファウル数が5回目以降、ファウルを犯す度に相手チームに2本のフリースローが与えられます。シュート動作に対するファウルかどうかは関係ありません。チームファウル数はクオーターごとにリセットされますが、延長戦の場合は第4クオーター終了時の累計チームファウル数が継続されます。
    </p>

    <h3>アンスポーツマンライクファウル</h3>

    <p>
      相手プレーヤーをわざと叩く・押しのける・蹴る・抱きかかえるといった行為に対してとられます。この場合、相手に2本のフリースローが与えられます。シュート動作中の場合、シュートが決まれば1本、外れた場合は2本のフリースローが与えられます。その後はスローインでゲームが再開します。
    </p>

    <h3>テクニカルファウル</h3>

    <p>
      コート内外を問わず、チームメンバーによる相手プレーヤーやレフェリーへの暴言・挑発、試合を遅らせる行為、ゴールやベンチなどを殴る・蹴るといった行為が該当します。この場合、チームファウル数に関係なく相手に1本のフリースローが与えられ、スローインで再開します。
    </p>

    <h3>退場</h3>

    <p>
      同じプレーヤーが、アンスポーツマンライクファウルやテクニカルファウルを2回以上とられた場合は退場です。ベンチには残れません。
    </p>

